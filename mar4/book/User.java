package mar4.book;

public class User {
    private String uname;
    private String uide;
    private int bnum;

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getUide() {
        return uide;
    }

    public void setUide(String uide) {
        this.uide = uide;
    }

    public int getBnum() {
        return bnum;
    }

    public void setBnum(int bnum) {
        this.bnum = bnum;
    }
}
